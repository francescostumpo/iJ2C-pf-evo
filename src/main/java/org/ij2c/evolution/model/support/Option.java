package org.ij2c.evolution.model.support;

public class Option {

    private String optionClause;
    private double optionValue;

    public String getOptionClause() {
        return optionClause;
    }

    public void setOptionClause(String optionClause) {
        this.optionClause = optionClause;
    }

    public double getOptionValue() {
        return optionValue;
    }

    public void setOptionValue(double optionValue) {
        this.optionValue = optionValue;
    }
}
